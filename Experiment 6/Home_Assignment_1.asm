CODE SEGMENT
ASSUME CS: CODE, DS: CODE

MAIN PROC
    
    MOV AX, 50
	MOV BX, 40
	CAll LCM
	
	MOV AX, 50
	MOV BX, 30
	CAll LCM 
	
	MOV AX, 50
	MOV BX, 20
	CAll LCM
	
	MOV AX, 50
	MOV BX, 10
	CAll LCM
HLT

LCM PROC
	PUSH AX
	PUSH BX
	MOV CX, 0FFFFH
LEV: XOR DX,DX
	DIV BX
	CMP DX, 0H
	JE VAL
	MOV AX, BX
	MOV BX, DX
	LOOP VAL
VAL:
	MOV CX, BX
	POP BX
	POP AX
	MUL BX
	MOV BX, CX
	DIV BX
	RET
LCM ENDP
	END MAIN
CODE ENDS
	END   
; The result is stored in BX.
